            <h4>Run-off cover: 3 years free</h4>
            <p>
                In the event of and with effect from the retirement or ceasing business practice of the Insured or in
                the event of death of the Insured, the Insured or his estate is granted an additional period of thirty
                six (36) months (hereinafter referred to as Run-off Cover) to identify and report claims and/or
                circumstances in connection with work performed during the currency of this policy that may give rise to
                a claim in terms of this policy, provided that the Insured enjoyed continuous uninterrupted professional
                indemnity cover with Hollard for a period of not less than three years immediately and consecutively
                prior to the expiring of this policy.
            </p>

            <p>If above conditions not met, annual run off cover can be purchased.</p>

